import pyautogui

pyautogui.size() # returns sizes of screen

pyautogui.position() # returns position of mouse on screen

pyautogui.moveTo(10, 10, duration=1.5) # moves mouse to location

pyautogui.moveRel(200, 0, duration=2) # moves mouse to the right

# Put mouse up in left corner to stop pyautogui program

